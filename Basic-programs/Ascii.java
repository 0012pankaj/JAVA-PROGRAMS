public class Ascii {
    public static void main(String[] args) {
        System.out.println("ASCII VALUE OF NUMBERS:-");
        for (int i = 48; i < 58; i++) {

            System.out.println((char) i + " Ascii value= " + i);
        }
        System.out.println("ASCII VALUE OF SMALL Alphabets:-");
        for (int i = 97; i < 122; i++) {

            System.out.println((char) i + " Ascii value= " + i);
        }
        System.out.println("ASCII VALUE OF NUMBERS:-");
        for (int i = 65; i < 90; i++) {

            System.out.println((char) i + " Ascii value= " + i);
        }
    }
}
